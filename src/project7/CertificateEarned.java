    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = jTable1.getSelectedRow();
        if (row == -1) {
        JOptionPane.showMessageDialog(this, "Select a certificate first!");
        return;
        }

    String certificateId = jTable1.getValueAt(row, 3).toString();
    Student s = this.currentStudent;

    for (Certificate c : s.getCertificates()) {
        if (c.getCertificateId().equals(certificateId)) {
            JOptionPane.showMessageDialog(this,
                "Certificate ID: " + c.getCertificateId() +
                "\nStudent: " + c.getStudentId() +
                "\nCourse ID: " + c.getCourseId() +             
                "\nIssued: " + c.getIssueDate()
                );
                return;
                 }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int row = jTable1.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Select a certificate!");
        return;
    }

    String certId = jTable1.getValueAt(row, 3).toString();

    Student s = this.currentStudent;

    for (Certificate c : s.getCertificates()) {
        if (c.getCertificateId().equals(certId)) {

            JSONObject json = new JSONObject();
            json.put("certificateId", c.getCertificateId());
            json.put("studentId", c.getStudentId());
              json.put("courseId", c.getCourseId());
            json.put("issueDate", c.getIssueDate());

            try {
                FileWriter fw = new FileWriter("certificate_" + certId + ".json");
                fw.write(json.toString(4));
                fw.close();

                JOptionPane.showMessageDialog(this, "Certificate downloaded!");
            } catch (Exception e) {
                e.printStackTrace();
            }

            return;
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed


