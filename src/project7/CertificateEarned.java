private void loadCertificates() {
    Student s = this.currentStudent;

    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);

    for (Certificate c : s.getCertificates()) {
        Object[] row = {
            c.getCertificateId(),
            c.getStudentId(),
            c.getCourseId(),
            c.getIssueDate()
        };
        model.addRow(row);
    }
    
}
